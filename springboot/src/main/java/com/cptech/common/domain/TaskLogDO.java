package com.cptech.common.domain;

import java.io.Serializable;

/**
 * 任务调度日志表
 * 
 * @author fb
 * @email 1595418464@qq.com
 * @date 2018-05-23 17:09:14
 */
public class TaskLogDO extends BaseDo implements Serializable {
	private static final long serialVersionUID = 1L;
	
	public static final String JOB_TYPE_SCHEDULER = "scheduler";
	public static final String JOB_TYPE_TRIGGER = "trigger";
	public static final String JOB_TYPE_JOB = "job";
	
	// 任务名称
	private String jobName;
	// 任务组名
	private String jobGroup;
	// 日志类型
	private String jobType;
	// 日志事件
	private String jobEvent;
	// 日志信息
	private String jobMessage;
	// 是否异常
	private String isException;
	// 异常信息
	private String exceptionInfo;

	/**
	 * 设置：任务名称
	 */
	public void setJobName(String jobName) {
		this.jobName = jobName;
	}

	/**
	 * 获取：任务名称
	 */
	public String getJobName() {
		return jobName;
	}

	/**
	 * 设置：任务组名
	 */
	public void setJobGroup(String jobGroup) {
		this.jobGroup = jobGroup;
	}

	/**
	 * 获取：任务组名
	 */
	public String getJobGroup() {
		return jobGroup;
	}

	/**
	 * 设置：日志类型
	 */
	public void setJobType(String jobType) {
		this.jobType = jobType;
	}

	/**
	 * 获取：日志类型
	 */
	public String getJobType() {
		return jobType;
	}

	/**
	 * 设置：日志事件
	 */
	public void setJobEvent(String jobEvent) {
		this.jobEvent = jobEvent;
	}

	/**
	 * 获取：日志事件
	 */
	public String getJobEvent() {
		return jobEvent;
	}

	/**
	 * 设置：日志信息
	 */
	public void setJobMessage(String jobMessage) {
		this.jobMessage = jobMessage;
	}

	/**
	 * 获取：日志信息
	 */
	public String getJobMessage() {
		return jobMessage;
	}

	/**
	 * 设置：是否异常
	 */
	public void setIsException(String isException) {
		this.isException = isException;
	}

	/**
	 * 获取：是否异常
	 */
	public String getIsException() {
		return isException;
	}

	/**
	 * 设置：异常信息
	 */
	public void setExceptionInfo(String exceptionInfo) {
		this.exceptionInfo = exceptionInfo;
	}

	/**
	 * 获取：异常信息
	 */
	public String getExceptionInfo() {
		return exceptionInfo;
	}
}
